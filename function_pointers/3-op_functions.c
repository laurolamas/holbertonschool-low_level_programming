#include "3-calc.h"

/**
 * op_add - Adds two number
 *
 * @a: a
 * @b: b
 *
 * Return: Sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subs two number
 *
 * @a: a
 * @b: b
 *
 * Return: Sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two number
 *
 * @a: a
 * @b: b
 *
 * Return: Mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two number
 *
 * @a: a
 * @b: b
 *
 * Return: Div
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - Remainder of two number
 *
 * @a: a
 * @b: b
 *
 * Return: Mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
